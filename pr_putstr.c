#include "ft_printf.h"

int		pr_putstr(const char *s)
{
	int	i;

	i = 0;
	if (s)
	{
		while (s[i] != '\0')
		{
			write(1, &s[i], 1);
			i++;
		}
		i--;
	}
	return (i);
}
